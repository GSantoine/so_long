#include <stddef.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	balance;

	if (n == 0)
		return (0);
	while (*s1 == *s2 && *s1 && *s2 && n > 1)
	{
		s1++;
		s2++;
		n--;
	}
	balance = (unsigned char)*s1 - (unsigned char)*s2;
	return (balance);
}
