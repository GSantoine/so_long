#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	size_t				i;
	unsigned char		*mem;

	mem = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		mem[i] = 0;
		i++;
	}
}
